/*Hàm gọi đến trang kết quả tìm kiếm*/ 
function handleSearchFileresult() {
    const searchTerm = document.getElementById('search').value;
    window.location.href = `ResultSearch.html?searchTerm=${searchTerm}`;
    return false; // Ngăn chặn việc gửi biểu mẫu
}

function ListSellingProducts(products) {
    let s = `<div class="item-selling-products" id="item-selling-products">`;

    for (let i = 0; i < products.length; i++) {
        const product = products[i];
        s += `
            <div class="item-show">
                <a href="#">
                    <img src="${product.img}" width="100%"/>
                </a>
                <div class="detail">
                    <h3 class="name-product">
                        <a id="name" href="#" title="${product.name}" tabindex="0">${product.name}</a>
                    </h3>
                    <div class="technical">
                        <div class="cpu">
                            <span>${product.cpu}</span>
                        </div>
                        <div class "vga">
                            <span>${product.vga}</span>
                        </div>
                        <div class="ram">
                            <span>${product.ram}</span>
                        </div>
                        <div class="ssd">
                            <span>${product.ssd}</span>
                        </div>
                        <div class="lcd">
                            <span>${product.lcd}</span>
                        </div>
                        <div class="price">
                            <div class="price--compare">
                                <del>${product.pricecompare}</del>
                            </div>
                            <div class="price--default">
                                <span class="price--highlight">${product.pricehighlight}</span>
                                <span class="label--on-sale">${product.labelonsale}</span>
                            </div>
                        </div>
                    </div>
                </div>
            </div>`;
    }

    s += `</div>`;
    document.getElementById("item-selling-products").innerHTML = s;
}

const urlParams = new URLSearchParams(window.location.search);
const searchTerm = urlParams.get('searchTerm');

if (searchTerm) {
    const searchResult = ArrayListProducts.filter(product => product.name.toLowerCase().includes(searchTerm.toLowerCase()));
    if (searchResult.length > 0) {
        ListSellingProducts(searchResult);
    } else {
        document.getElementById('item-selling-products').innerText = "Không tìm thấy kết quả phù hợp.";
    }
} else {
    ListSellingProducts(ArrayListProducts);
}






